class Product
{
  //Attributes
  String ?Name;
  String ?Price;
  String ?Description;
  String ?Asseturl;
  //Constructor
  Product (String Name , String Price , String Description , String Asseturl)
  {
    this.Name = Name;
    this.Price = Price;
    this.Description = Description;
    this.Asseturl = Asseturl;
  }
}